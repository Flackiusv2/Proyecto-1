package pieza;

public class Pintura {

	private int alto;
	private int ancho;
	private String tecnica;
	public Pintura(int alto, int ancho, String tecnica) {
		super();
		this.alto = alto;
		this.ancho = ancho;
		this.tecnica = tecnica;
	}
	public int getAlto() {
		return alto;
	}
	public int getAncho() {
		return ancho;
	}
	public String getTecnica() {
		return tecnica;
	}
	
	
	
}
